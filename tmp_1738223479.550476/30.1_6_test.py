from scicode.parse.parse import process_hdf5_to_tuple
import numpy as np

import numpy as np




class Slater:
    def __init__(self, alpha):
        '''Args: 
            alpha: exponential decay factor
        '''
        self.alpha = alpha

    def _compute_magnitudes(self, configs):
        return np.linalg.norm(configs, axis=2)

    def value(self, configs):
        '''Calculate unnormalized psi
        Args:
            configs (np.array): electron coordinates of shape (nconf, nelec, ndim)
        Returns:
            val (np.array): (nconf,)
        '''
        magnitudes = self._compute_magnitudes(configs)
        psi = np.exp(-self.alpha * np.sum(magnitudes, axis=1))
        return psi
    
    def gradient(self, configs):
        '''Calculate (gradient psi) / psi
        Args:
            configs (np.array): electron coordinates of shape (nconf, nelec, ndim)
        Returns:
            grad (np.array): (nconf, nelec, ndim)
        '''
        magnitudes = self._compute_magnitudes(configs)[:, :, np.newaxis]
        grad = -self.alpha * configs / magnitudes
        return grad

    def laplacian(self, configs):
        '''Calculate (laplacian psi) / psi
        Args:
            configs (np.array): electron coordinates of shape (nconf, nelec, ndim)
        Returns:
            lap (np.array): (nconf, nelec)
        '''
        magnitudes = self._compute_magnitudes(configs)
        lap = (self.alpha**2 * magnitudes - 2 * self.alpha) / (magnitudes**2)
        return lap

    def kinetic(self, configs):
        '''Calculate the kinetic energy / psi
        Args:
            configs (np.array): electron coordinates of shape (nconf, nelec, ndim)
        Returns:
            kin (np.array): (nconf,)
        '''
        lap = self.laplacian(configs)
        kinetic_energy = -0.5 * np.sum(lap, axis=1)
        return kinetic_energy


