class Slater:
    def __init__(self, alpha):
        '''Args: 
            alpha: exponential decay factor
        '''
        self.alpha = alpha

    def value(self, configs):
        '''Calculate unnormalized psi
        Args:
            configs (np.array): electron coordinates of shape (nconf, nelec, ndim)
        Returns:
            val (np.array): (nconf,)
        '''
        r = np.sqrt(np.sum(configs**2, axis=2))
        val = np.exp(-self.alpha * np.sum(r, axis=1))
        return val

    def gradient(self, configs):
        '''Calculate (gradient psi) / psi
        Args:
            configs (np.array): electron coordinates of shape (nconf, nelec, ndim)
        Returns:
            grad (np.array): (nconf, nelec, ndim)
        '''
        r = np.sqrt(np.sum(configs**2, axis=2))
        grad = np.full(configs.shape, -self.alpha)
        r_nonzero = r != 0
        grad[r_nonzero] *= configs[r_nonzero] / r[r_nonzero][:, :, np.newaxis]
        return grad

    def laplacian(self, configs):
        '''Calculate (laplacian psi) / psi
        Args:
            configs (np.array): electron coordinates of shape (nconf, nelec, ndim)
        Returns:
            lap (np.array): (nconf, nelec)
        '''
        r = np.sqrt(np.sum(configs**2, axis=2))
        lap = np.full(r.shape, self.alpha**2)
        r_nonzero = r != 0
        lap[r_nonzero] -= 2 * self.alpha / r[r_nonzero]
        return lap

    def kinetic(self, configs):
        '''Calculate the kinetic energy
        Args:
            configs (np.array): electron coordinates of shape (nconf, nelec, ndim)
        Returns:
            kin (np.array): (nconf,)
        '''
        lap = self.laplacian(configs)
        kin = -0.5 * np.sum(lap, axis=1)
        return kin



# Background: 
# The Jastrow factor is a correlation function used in quantum mechanics to include electron-electron interactions in wave functions.
# For a two-electron system, the Jastrow factor can be expressed as an exponential function of the inter-electronic distance.
# The Jastrow wave function is given by exp(beta * |r1 - r2|), where r1 and r2 are the position vectors of the two electrons.
# This wave function modifies the overall wave function to better account for electron correlation effects.
# We need to compute the value of the Jastrow factor, its gradient, and its Laplacian.
# The gradient of the Jastrow factor with respect to the electron positions is derived based on the chain rule,
# and the Laplacian involves the divergence of the gradient, accounting for electron-electron correlation.


class Jastrow:
    def __init__(self, beta=1):
        '''
        Args:
            beta: a parameter controlling the strength of the correlation
        '''
        self.beta = beta

    def get_r_vec(self, configs):
        '''Returns a vector pointing from r2 to r1, which is r_12 = [x1 - x2, y1 - y2, z1 - z2].
        Args:
            configs (np.array): electron coordinates of shape (nconf, nelec, ndim)
        Returns:
            r_vec (np.array): (nconf, ndim)
        '''
        r_vec = configs[:, 0, :] - configs[:, 1, :]
        return r_vec

    def get_r_ee(self, configs):
        '''Returns the Euclidean distance from r2 to r1
        Args:
            configs (np.array): electron coordinates of shape (nconf, nelec, ndim)
        Returns:
            r_ee (np.array): (nconf,)
        '''
        r_vec = self.get_r_vec(configs)
        r_ee = np.sqrt(np.sum(r_vec**2, axis=1))
        return r_ee

    def value(self, configs):
        '''Calculate Jastrow factor
        Args:
            configs (np.array): electron coordinates of shape (nconf, nelec, ndim)
        Returns 
            jast (np.array): (nconf,)
        '''
        r_ee = self.get_r_ee(configs)
        jast = np.exp(self.beta * r_ee)
        return jast

    def gradient(self, configs):
        '''Calculate (gradient psi) / psi
        Args:
            configs (np.array): electron coordinates of shape (nconf, nelec, ndim)
        Returns:
            grad (np.array): (nconf, nelec, ndim)
        '''
        r_ee = self.get_r_ee(configs)
        r_vec = self.get_r_vec(configs)
        
        grad = np.zeros_like(configs)
        r_nonzero = r_ee != 0
        
        grad[r_nonzero, 0, :] = self.beta * r_vec[r_nonzero] / r_ee[r_nonzero][:, np.newaxis]
        grad[r_nonzero, 1, :] = -self.beta * r_vec[r_nonzero] / r_ee[r_nonzero][:, np.newaxis]
        
        return grad

    def laplacian(self, configs):
        '''Calculate (laplacian psi) / psi
        Args:
            configs (np.array): electron coordinates of shape (nconf, nelec, ndim)
        Returns:
            lap (np.array):  (nconf, nelec)
        '''
        r_ee = self.get_r_ee(configs)
        r_vec = self.get_r_vec(configs)
        ndim = configs.shape[2]

        lap = np.zeros((configs.shape[0], 2))
        r_nonzero = r_ee != 0
        
        lap[r_nonzero, 0] = self.beta * (ndim - 1) / r_ee[r_nonzero]
        lap[r_nonzero, 1] = self.beta * (ndim - 1) / r_ee[r_nonzero]
        
        lap[r_nonzero, 0] -= self.beta**2
        lap[r_nonzero, 1] -= self.beta**2

        return lap


